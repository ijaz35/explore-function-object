var color = 'green';
switch (color) {
    case 'blue':
        console.log("color is blue");
        break;
    case 'black':
    case 'paste':
        console.log("color is black or paste");
        break;
    case 'gray':
        console.log("color is gray");
        break;
    case 'red':
        console.log("color is red");
        break;
    case 'white':
        console.log("color is white");
        break;
    case 'yellow':
        console.log("color is yellow");
        break;
    case 'violet':
        console.log("color is violet");
        break;
    default:
        console.log("it's a default color");

}